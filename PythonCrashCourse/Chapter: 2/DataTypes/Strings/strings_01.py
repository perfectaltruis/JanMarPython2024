# Adding whitespace, new line and tabs

# to add a new line in a sentence use a "\n" escape character
# to add a tab in a sentence use a "\t" escape character
# EXAMPLE

print("This is a normal line with no any escape character")
print("This is else a normal line")

print("\tTab is inserted here at the start of the paragraph")
print("There is a tab here\t\tThis is not good")

# new lines
print("TO insert a new line add this\nand the sentence will start at a new line")

print("My best programming language are: \n\tC\n\tPython\n\tJava")
